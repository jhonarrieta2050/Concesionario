    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = nombreText.getText();
        String apellido = apellidoText.getText();
        String correo = correoText.getText();
        String contrasena = contrasenaText.getText();
        
        boolean pass = ControladorVerificador.verificarUsuario(nombre, apellido, correo, contrasena);
        if(!pass){
            errorText.setText("Datos invalidos");
            return;
        }
        
        comand.guardarUsuario(new Usuario(nombre, apellido, correo, contrasena));
        
        this.setVisible(false);
        loginUsuarioGui.setVisible(true);
        loginUsuarioGui.setLocationRelativeTo(null);
        
    }//GEN-LAST:event_jButton1ActionPerformed

